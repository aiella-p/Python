# Iterate a List using while loop
# Python list is an ordered sequence of items. It is ordered by index numbers starting from 0. It is enclosed by square the ‘[]’ brackets. Here are some of the examples of lists.  

numbers = [1, 2, 4, 5, 7]
size = len(numbers)
i = 0
while i < size:
    print(numbers[i])
    i = i + 1