# We create a function in Python to perform a specific task.
# The function can return a value that is nothing but an output of function execution.

# Using a return statement, we can return a value from a function when called.

# Define a function
# function acceptts two numbers and return their sum
def addition(num1, num2):
    return num1 + num2  # return the sum of two numbers

# result is the return value
result = addition(10, 20)
print(result)

# result is the return value
def subtraction(num1,num2):
    return num1 - num2
result = subtraction(10, 20)
print(result)

# result is the return value
def mul(num1,num2):
    return num1 * num2
result = mul(10, 20)
print(result)

# result is the return value
def div(num1,num2):
    return num1/num2
result = div(10, 20)
print(result)

# result is the return value
def subtraction(num1,num2):
    return num1 //  num2
result = subtraction(101, 20)
