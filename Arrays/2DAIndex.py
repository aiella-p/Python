import numpy as np
arr2 = np.array([[1,2,3,4],[5,6,7,8]])
print(arr2)

# indexing the 2D array...
# Second element in second row...

print("Second element in first row: " , arr2[0,1])

# Fourth element in first row...
print("Fourth element in first row: " , arr2[0,3])

# Third element in the Second row...
print("Third element in Second row: " , arr2[1,2])

# Fourth element in the Second row...
print("Third element in Second row: " , arr2[1,3])

# First element in the Second row...
print("First element in Second row: " , arr2[1,0])
